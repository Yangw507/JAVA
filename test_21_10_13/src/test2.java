public class test2 {

    //打印数组的方法
    public static void printArray(int[] arr) {
        for (int x:arr) {
            System.out.print(x+" ");
        }
    }

    public static void main(String[] args) {
        int[] arr = {1,2,3,4};
        printArray(arr);
    }
}
